<?php

namespace App\Http\Controllers;

use App\Models\Product;
use Illuminate\Http\Request;
use Illuminate\Support\Facades\Session;

class CartController extends Controller
{
    /*
    |--------------------------------------------------------------------------
    | カート一覧
    |--------------------------------------------------------------------------
    */

    public function index()
    {
        $cart = Session::get('cart', []);

        $total = collect($cart)->sum(function ($item) {
            return $item['price'] * $item['quantity'];
        });

        return view('cart.index', [
            'cart'  => $cart,
            'total' => $total,
        ]);
    }

    /*
    |--------------------------------------------------------------------------
    | カートへ追加
    |--------------------------------------------------------------------------
    */

    public function add(Request $request, Product $product)
    {
        $quantity = max(1, (int) $request->input('quantity', 1));

        /*
        |--------------------------------------------------------------------------
        | 商品販売チェック
        |--------------------------------------------------------------------------
        */

        if (! $product->isAvailableForSale()) {

            return back()->with(
                'error',
                '現在この商品は購入できません。'
            );
        }

        /*
        |--------------------------------------------------------------------------
        | 在庫チェック
        |--------------------------------------------------------------------------
        */

        $stock = $product->totalStock();

        if ($stock <= 0) {

            return back()->with(
                'error',
                '在庫切れの商品です。'
            );
        }

        /*
        |--------------------------------------------------------------------------
        | セッション取得
        |--------------------------------------------------------------------------
        */

        $cart = Session::get('cart', []);

        $key = (string) $product->id;
                /*
        |--------------------------------------------------------------------------
        | 既にカートへ入っている場合
        |--------------------------------------------------------------------------
        */

        if (isset($cart[$key])) {

            $newQuantity = $cart[$key]['quantity'] + $quantity;

            /*
            |--------------------------------------------------------------------------
            | 在庫以上は入れられない
            |--------------------------------------------------------------------------
            */

            if ($newQuantity > $stock) {

                return back()->with(
                    'error',
                    "在庫数を超えています。（在庫：{$stock}個）"
                );
            }

            $cart[$key]['quantity'] = $newQuantity;

        } else {

            /*
            |--------------------------------------------------------------------------
            | 新規追加時の在庫チェック
            |--------------------------------------------------------------------------
            */

            if ($quantity > $stock) {

                return back()->with(
                    'error',
                    "在庫数を超えています。（在庫：{$stock}個）"
                );
            }

            $cart[$key] = [

                'id' => $product->id,

                'name' => $product->name,

                'price' => (int) $product->price,

                'quantity' => $quantity,

            ];
        }

        /*
        |--------------------------------------------------------------------------
        | セッション保存
        |--------------------------------------------------------------------------
        */

        Session::put('cart', $cart);

        return redirect()
            ->route('cart.index')
            ->with('success', 'カートへ追加しました。');
    }

    /*
    |--------------------------------------------------------------------------
    | 数量変更
    |--------------------------------------------------------------------------
    */

    public function update(Request $request)
    {
        $cart = Session::get('cart', []);

        $key = $request->input('key');

        $quantity = max(
            1,
            (int) $request->input('quantity', 1)
        );
                /*
        |--------------------------------------------------------------------------
        | カート内に存在するか
        |--------------------------------------------------------------------------
        */

        if (! isset($cart[$key])) {

            return response()->json([
                'success' => false,
                'message' => '商品が見つかりません。',
            ], 404);
        }

        /*
        |--------------------------------------------------------------------------
        | 商品取得
        |--------------------------------------------------------------------------
        */

        $product = Product::find($cart[$key]['id']);

        if (! $product) {

            unset($cart[$key]);

            Session::put('cart', $cart);

            return response()->json([
                'success' => false,
                'message' => '商品が削除されました。',
            ], 404);
        }

        /*
        |--------------------------------------------------------------------------
        | 販売可能チェック
        |--------------------------------------------------------------------------
        */

        if (! $product->isAvailableForSale()) {

            return response()->json([
                'success' => false,
                'message' => '現在購入できない商品です。',
            ], 400);
        }

        /*
        |--------------------------------------------------------------------------
        | 在庫チェック
        |--------------------------------------------------------------------------
        */

        $stock = $product->totalStock();

        if ($stock <= 0) {

            return response()->json([
                'success' => false,
                'message' => '在庫切れです。',
            ], 400);
        }

        /*
        |--------------------------------------------------------------------------
        | 在庫以上なら在庫数まで補正
        |--------------------------------------------------------------------------
        */

        if ($quantity > $stock) {

            $quantity = $stock;
        }

        /*
        |--------------------------------------------------------------------------
        | 数量更新
        |--------------------------------------------------------------------------
        */

        $cart[$key]['quantity'] = $quantity;

        Session::put('cart', $cart);

        return response()->json([
            'success' => true,
            'quantity' => $quantity,
            'subtotal' => $cart[$key]['price'] * $quantity,
            'message' => '数量を更新しました。',
        ]);
    }

    /*
    |--------------------------------------------------------------------------
    | カートから削除
    |--------------------------------------------------------------------------
    */

    public function remove(Request $request)
    {
            /*
        |--------------------------------------------------------------------------
        | キー取得
        |--------------------------------------------------------------------------
        */

        $key = $request->input('key');

        $cart = Session::get('cart', []);

        /*
        |--------------------------------------------------------------------------
        | 存在する商品のみ削除
        |--------------------------------------------------------------------------
        */

        if (isset($cart[$key])) {

            unset($cart[$key]);

            Session::put('cart', $cart);

            return redirect()
                ->route('cart.index')
                ->with('success', '商品をカートから削除しました。');
        }

        return redirect()
            ->route('cart.index')
            ->with('error', '商品が見つかりませんでした。');
    }
}
